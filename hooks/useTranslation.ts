export function useTranslation() {
  const { language, toggleLanguage } = useSettingsStore();

  const t = (key: string): string => {
    return translations[language][key as keyof typeof translations['ar']] || key;
  };

  return {
    t,
    language,
    toggleLanguage,
  };
}